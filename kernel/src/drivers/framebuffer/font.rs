//! Tiny 8x16 bitmap font for boot diagnostics.
//!
//! Only a subset of printable ASCII is populated with real glyphs — enough
//! to render boot banners. Unmapped characters show an outlined box.
//!
//! Each glyph is 16 rows of 8 bits; bit 0x80 is the leftmost pixel.

#![allow(non_upper_case_globals)]

const MISSING: [u8; 16] = [
    0x00, 0x7E, 0x42, 0x42, 0x42, 0x42, 0x42, 0x42, 0x42, 0x42, 0x42, 0x42, 0x7E, 0x00, 0x00, 0x00,
];

const SPACE: [u8; 16] = [0; 16];

macro_rules! g {
    ($($row:expr),+ $(,)?) => {{
        let rows = [$($row),+];
        let mut out = [0u8; 16];
        let mut i = 0;
        while i < rows.len() && i < 16 {
            out[i] = rows[i];
            i += 1;
        }
        out
    }};
}

