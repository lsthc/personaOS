/// Look up the glyph for a character. Unknown characters render as an
/// outlined box.
pub fn glyph(ch: char) -> [u8; 16] {
    match ch {
        ' ' => SPACE,
        'M' => LETTER_M,
        'O' => LETTER_O,
        'S' => LETTER_S,
        'a' => LETTER_a,
        'b' => LETTER_b,
        'd' => LETTER_d,
        'e' => LETTER_e,
        'f' => LETTER_f,
        'k' => LETTER_k,
        'l' => LETTER_l,
        'm' => LETTER_m,
        'n' => LETTER_n,
        'o' => LETTER_o,
        'p' => LETTER_p,
        'r' => LETTER_r,
        's' => LETTER_s,
        't' => LETTER_t,
        'u' => LETTER_u,
        'y' => LETTER_y,
        '0' => DIGIT_M0,
        '1' => DIGIT_1,
        '-' => DASH,
        ',' => COMMA,
        '.' => DOT,
        _ => MISSING,
    }
}

